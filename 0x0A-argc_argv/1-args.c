#include <stdio.h>

/**
* main - program to print the number of command
* line args
* @argc: count of command line args
* @argv: pointer to array of strings of all
* command line args
*
* Return: always 0
*/

int main(int argc, char *argv[])
{
	printf("%d\n", --argc);
	return (0 * sizeof(*argv));
}
